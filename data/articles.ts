function loadGeneratedArticles(): Article[] {
  const dir = join(process.cwd(), 'data', 'generated');
  if (!existsSync(dir)) return [];
  try {
    return readdirSync(dir)
      .filter((f) => f.endsWith('.json'))
      .sort()
      .reverse()
      .map((f) => JSON.parse(readFileSync(join(dir, f), 'utf-8')) as Article);
  } catch {
    return [];
  }
}

